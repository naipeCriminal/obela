					<p>Las personas que padecen diabetes tienen que considerar el índice glicémico en los alimentos, el cual es una clasificación de los carbohidratos en una escala de 0 a 100 de acuerdo al grado en que elevan los niveles de azúcar en la sangre después de comer. Los alimentos con los índices más altos elevan los niveles de azúcar en la sangre más rápidamente que los que tienen un menor índice glicémico. Para las personas con diabetes es importante controlar sus niveles de glucosa, el índice glicémico del Hummus es bajo (una porción de 30 g tiene un índice glicémico de 6) siendo éste adecuado para ser consumido en porciones correctas por una persona con diabetes.</p>
